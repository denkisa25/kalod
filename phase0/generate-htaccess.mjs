#!/usr/bin/env node
/**
 * Generates public/.htaccess from phase0/extraction/redirect-map.csv.
 * cPanel/Apache hosting (not Cloudflare Pages), so this is a "Redirect 301"
 * .htaccess rather than a Cloudflare _redirects file. Astro copies
 * public/.htaccess into dist/ untouched, and it lands at the site root
 * on deploy (see README.md "deploy" section).
 *
 * Regenerate after editing redirect-map.csv:
 *   node phase0/generate-htaccess.mjs
 */
import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const here = dirname(fileURLToPath(import.meta.url));
const csvPath = join(here, 'extraction/redirect-map.csv');
const outPath = join(here, '../public/.htaccess');

const rows = readFileSync(csvPath, 'utf8')
  .trim()
  .split('\n')
  .slice(1) // header
  .map((line) => line.split(','));

const normalize = (p) => (p.endsWith('/') ? p : `${p}/`);

const redirectLines = rows.map(([oldPath, newPath]) => `Redirect 301 ${oldPath} ${normalize(newPath)}`);

const htaccess = `# Generated by phase0/generate-htaccess.mjs from redirect-map.csv — do not
# hand-edit; fix the CSV and regenerate instead.
#
# ${rows.length} redirects from the old WordPress site (spec §13). The
# domain has a decade of search authority — these must ship with launch.

${redirectLines.join('\n')}

# static-site fallbacks
ErrorDocument 404 /404.html

<IfModule mod_deflate.c>
  AddOutputFilterByType DEFLATE text/html text/css application/javascript application/json image/svg+xml
</IfModule>

<IfModule mod_expires.c>
  ExpiresActive On
  ExpiresByType image/jpeg "access plus 1 year"
  ExpiresByType image/png "access plus 1 year"
  ExpiresByType image/webp "access plus 1 year"
  ExpiresByType font/woff "access plus 1 year"
  ExpiresByType text/css "access plus 1 month"
  ExpiresByType application/javascript "access plus 1 month"
</IfModule>
`;

writeFileSync(outPath, htaccess);
console.log(`Wrote ${rows.length} redirects to ${outPath}`);
